def sum3(first_number, second_number, third_number):
    """
    Takes three numbers and returns the sum of those numbers.

    Parameters:
    first_number (int or float): The first number.
    second_number (int or float): The second number.
    third_number (int or float): The third number.

    Returns:
    int or float: The sum of the three numbers.
    """
    result = first_number + second_number + third_number
    return result
